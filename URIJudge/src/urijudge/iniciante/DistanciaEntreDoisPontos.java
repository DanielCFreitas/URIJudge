package urijudge.iniciante;

import java.util.Scanner;

public class DistanciaEntreDoisPontos {

	public static void main(String[] args) {
		double ponto1_x, ponto1_y, ponto2_x, ponto2_y;
		Scanner read = new Scanner(System.in);

		ponto1_x = read.nextDouble();
		ponto1_y = read.nextDouble();
		ponto2_x = read.nextDouble();
		ponto2_y = read.nextDouble();
		read.close();

		System.out.printf("%.4f\n", Math.sqrt(Math.pow(ponto2_x - ponto1_x, 2) + Math.pow(ponto2_y - ponto1_y, 2)));
	}

}
